    private void jb_UpdateButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_UpdateButtonMouseClicked
        // TODO add your handling code here:
        Player p = logger.getLoggedInPlayer();
        
        String newName = jtf_NameChangerField.getText();
        String newPass = jtf_PassChangerField.getText();
        
        if (newName.isBlank() || newPass.isBlank())
        {
            JOptionPane.showMessageDialog(this, "Please do not leave empty spaces.");
        } else
        {
            p.setUsername(newName);
            p.setPassword(newPass);
            JOptionPane.showMessageDialog(this, "Profile data updated successfully");
            logger.listPlayers();
            jp_ModifyPanel.setVisible(false);
            jp_ProfileDataPanel.setVisible(true);
            
            
            
            jl_UsernameGoesHere.setText(p.getUsername());
            jl_PasswordGoesHere.setText(p.getPassword());
            jl_ScoreGoesHere.setText(p.getPlayerScore() + " points");
        }
        

        
    }//GEN-LAST:event_jb_UpdateButtonMouseClicked

