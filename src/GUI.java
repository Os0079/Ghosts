    private void jb_10GamesButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_10GamesButtonMouseClicked
        // TODO add your handling code here:
        jp_ReportsPanel.setVisible(true);
        jp_10GamesPanel.setVisible(true);
        Player p = logger.getLoggedInPlayer();
        
         //jt_10GamesTable.setModel(new DefaultTableModel(null, new String[]{"Game Description"}));
        
         
        Object rowData[] = new Object[1];
        DefaultTableModel tblm = (DefaultTableModel) jt_10GamesTable.getModel();

        if (!alreadyInTable) 
        {
            for (int i = 0; i < p.getGameReports().length; i++)
            {
                rowData[0] = p.getGameReports()[i].getDescription();

                tblm.addRow(rowData);
                alreadyInTable = true;
            }
        }
            
        
    }//GEN-LAST:event_jb_10GamesButtonMouseClicked

