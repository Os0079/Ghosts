    private void jb_LGLoginButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_LGLoginButtonMouseClicked
        // TODO add your handling code here:
        String nombre = jtf_LGNameField.getText();
        String contra = jtf_LGPasswordField.getText();

        if (nombre.isBlank() || contra.isBlank()) 
        {
            JOptionPane.showMessageDialog(this, "Please do not leave empty spaces.");
        } else 
        {
            if (logger.search(nombre) == null) 
            {
                JOptionPane.showMessageDialog(this, "This user does not exist.");
            } else 
            {
                Player p = logger.search(nombre);
                p.setLoggedIn(logger.login(nombre, contra));
                
                if (p.isLoggedIn())
                {
                    
                    JOptionPane.showMessageDialog(this, "Starting...");
                    
                    /*
                    jp_LGBackground.setVisible(false);
                    jp_MainMenu.setVisible(true);
                    */
                    
                    cardLayout.show(jp_CardHandler, "MainMenuCard");

                }else
                {
                    JOptionPane.showMessageDialog(this, "Wrong password.");
                }
                
                logger.listPlayers();
            }
        }
    }//GEN-LAST:event_jb_LGLoginButtonMouseClicked

