    private void jb_SUSignUpButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_SUSignUpButtonMouseClicked
        // TODO add your handling code here:
        String nombre = jtf_SUNameField.getText();
        String contra = jtf_SUPasswordField.getText();
        
        if (nombre.isBlank() || contra.isBlank() )
        {
            JOptionPane.showMessageDialog(this, "Please do not leave empty spaces");
        } else
        {
            if ( logger.search(nombre) != null)
            {
                JOptionPane.showMessageDialog(this, "This user already exists.");
            } else
            {
                Player p = new Player(nombre, contra);
                logger.addPlayer(p);
                p.setLoggedIn(logger.login(nombre, contra));
                JOptionPane.showMessageDialog(this, "User added successfully.");

                /*
                jp_SUBackground.setVisible(false);
                jp_MainMenu.setVisible(true);
                */
                
                cardLayout.show(jp_CardHandler, "MainMenuCard");

                logger.listPlayers();
            }
        }
    }//GEN-LAST:event_jb_SUSignUpButtonMouseClicked

